    private void tblSearchTenantMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSearchTenantMouseClicked
        int rowno = tblSearchTenant.getSelectedRow();
        TableModel modle = tblSearchTenant.getModel();
        txtid.setText(model.getValueAt(rowno, 0).toString());
        txtname.setText((String) model.getValueAt(rowno, 1));
        txtPhoneno.setText(modle.getValueAt(rowno, 2).toString());
        txtroomno.setText(model.getValueAt(rowno, 3).toString());
        txtmonthlyrent.setText(model.getValueAt(rowno, 4).toString());
        txtstartdate.setText(model.getValueAt(rowno, 5).toString());
        comboxStatus.setSelectedItem(model.getValueAt(rowno, 6));

    }//GEN-LAST:event_tblSearchTenantMouseClicked

