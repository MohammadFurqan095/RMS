    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) tblSearchTenant.getModel();
        model.setRowCount(0);
    }

    public void getAllTenant() {
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("Select * from tenant");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                String phoneNo = rs.getString("phone_no");
                String roomNo = rs.getString("room_no");
                double monthlyRent = rs.getDouble("monthly_rent");
                String startDate = rs.getString("start_date");
                String status = rs.getString("status");

                Object[] obj = {id, name, phoneNo, roomNo, monthlyRent, startDate, status};
                model = (DefaultTableModel) tblSearchTenant.getModel();
                model.addRow(obj);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void update(int id, String name, String phoneNO, String roomNo, double monthlyRent, String startDate, String status) {
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("update tenant set name = ?, phone_no = ?,"
                    + "room_no=?,monthly_rent=?,start_date=?,status=? where id = ?");
            pst.setString(1, name);
            pst.setString(2, phoneNO);
            pst.setString(3, roomNo);
            pst.setDouble(4, monthlyRent);
            pst.setString(5, startDate);
            pst.setString(6, status);
            pst.setInt(7, id);
            int rowCount = pst.executeUpdate();
            if (rowCount == 1) {
                JOptionPane.showMessageDialog(this, "Tenant update sussccefully.");
                clearTable();
                getAllTenant();
            } else {
                JOptionPane.showMessageDialog(this, "Tenant updation failed.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void delete(int id) {
        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("update tenant set status='Unavailable' where id = ?");

            pst.setInt(1, id);
            int rowcount = pst.executeUpdate();
            if (rowcount == 1) {
                JOptionPane.showMessageDialog(this, "Tenant Delete succesfully.");
                clearTable();
                getAllTenant();
            } else {
                JOptionPane.showMessageDialog(this, "Tenant deletion failed.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tenant deletion failed.");
            e.printStackTrace();
        }
    }

